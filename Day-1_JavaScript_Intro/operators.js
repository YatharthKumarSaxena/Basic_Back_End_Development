// Arithmetic Operator
console.log(5+4)
console.log(2*4)
console.log(3-5)
console.log(4/5)
console.log(9%4) // Modulo % Operator
console.log(2**5) // ** For Exponent

// Increment Operator
var i = 1
console.log(i++) // Post Increment Operator
console.log(++i) // Pre Increment Operator

// Decrement Operator
var i = 1
console.log(i--) // Post Decrement Operator
console.log(--i) // Pre Decrement Operator

// Relational Operator

// 1. Strict equality check :- Check Type and Value both
console.log(1==='1')
// 2. Strict inequality check :- Check Type and Value both
console.log(1!=='1')
// 3. Loose equality check :- Check Value only not type
console.log(1=='1')
// 4. Looose inequality check :- Check Value only not type
console.log(1!='1')

